// Minimal Option B PDF service using Puppeteer-like interface.
// Note: No network or actual installation here; this file documents a simple API and local harness.
// All comments in English only.

import { createServer } from 'http'
import { Buffer } from 'buffer'

export interface PdfRequest {
  title?: string
  manifest: {
    meta?: any
    sections: Array<{ title: string; type: 'table'|'image'|'text'; payload: any }>
  }
}

export function startServer(port = 8787) {
  const srv = createServer(async (req, res) => {
    if (req.method === 'POST' && req.url === '/render/pdf') {
      let body = ''
      req.on('data', (c) => { body += c })
      req.on('end', async () => {
        try {
          const parsed = JSON.parse(body) as PdfRequest
          const suggestedName = (parsed.title?.trim() || 'report').replace(/[^a-zA-Z0-9._-]+/g, '_')
          const usePuppeteer = process.env.PDF_RENDERER === 'puppeteer'
          if (!usePuppeteer) {
            // Return a tiny but valid PDF header in stub mode
            const pdfBytes = Buffer.from('%PDF-1.4\n%\xFF\xFF\n1 0 obj\n<<>>\nendobj\ntrailer\n<<>>\n%%EOF')
            res.writeHead(200, { 'Content-Type': 'application/pdf', 'Content-Disposition': `attachment; filename="${suggestedName}.pdf"` })
            res.end(pdfBytes)
            return
          }
          // Lazy import puppeteer only when enabled via env to keep dev lean
          const puppeteer = await import('puppeteer')
          const browser = await puppeteer.launch({ args: ['--no-sandbox', '--disable-setuid-sandbox'] })
          const page = await browser.newPage()
          // Compose a minimal HTML with header/footer markers and a table for content
          const styles = `
            <style>
              body { font-family: system-ui, sans-serif; padding: 24px; }
              header, footer { font-size: 12px; color: #555; }
              table { border-collapse: collapse; width: 100%; }
              td, th { border: 1px solid #ddd; padding: 6px; }
              .cover { page-break-after: always; display: flex; height: 80vh; align-items: center; justify-content: center; }
            </style>
          `
          const headerText = parsed.manifest?.meta?.branding?.title || parsed.title || 'Report'
          const footerText = parsed.manifest?.meta?.branding?.footer || 'Generated by Insights Portal'
          const wantCover = !!parsed.manifest?.meta?.cover
          const rows = (parsed.manifest?.sections || [])
            .filter((s) => s.type === 'table' && Array.isArray(s.payload))
            .flatMap((s) => s.payload as Array<Record<string, any>>)
          const cols = Array.from(new Set(rows.flatMap((r) => Object.keys(r))))
          const tableHtml = rows.length
            ? `<table><thead><tr>${cols.map((c) => `<th>${c}</th>`).join('')}</tr></thead><tbody>${rows
                .map((r) => `<tr>${cols.map((c) => `<td>${r[c] ?? ''}</td>`).join('')}</tr>`)
                .join('')}</tbody></table>`
            : '<div>No table data</div>'
          const coverHtml = wantCover ? `<div class="cover"><h1>${headerText}</h1></div>` : ''
          const html = `<!doctype html><html><head>${styles}</head><body>
            <header id="pdf-header" data-testid="pdf-header">${headerText}</header>
            ${coverHtml}
            <h2>Contents</h2>
            ${tableHtml}
            <footer id="pdf-footer" data-testid="pdf-footer">${footerText}</footer>
          </body></html>`
          await page.setContent(html, { waitUntil: 'networkidle0' })
          const pdfBuf = await page.pdf({
            format: 'A4',
            printBackground: true,
            displayHeaderFooter: true,
            headerTemplate: `<div style="font-size:10px; width:100%; text-align:center;">${headerText}</div>`,
            footerTemplate: `<div style="font-size:10px; width:100%; text-align:center;">${footerText} — <span class="pageNumber"></span>/<span class="totalPages"></span></div>`,
            margin: { top: '20mm', bottom: '16mm', left: '12mm', right: '12mm' },
          })
          // Prepare meta header to facilitate golden checks without parsing PDF bytes
          const meta = {
            header: headerText,
            footer: footerText,
            tableRows: rows.length,
            format: 'A4',
            headerFooter: true,
            cover: wantCover,
            sections: (parsed.manifest?.sections || []).length,
            minPages: wantCover ? 2 : 1,
          }
          await browser.close()
          res.writeHead(200, {
            'Content-Type': 'application/pdf',
            'Content-Disposition': `attachment; filename="${suggestedName}.pdf"`,
            'X-PDF-Info': JSON.stringify(meta),
          })
          res.end(pdfBuf)
        } catch (e: any) {
          res.statusCode = 400
          res.end(String(e?.message || e))
        }
      })
      return
    }
    res.statusCode = 404
    res.end('Not Found')
  })
  srv.listen(port)
  return srv
}

